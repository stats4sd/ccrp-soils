<p>The platform can be used in different ways, and you can choose how many of the available tools you want to use based on your needs.</p>
<p>To learn more about what the platform does and how your project can get involved, see the &#8220;Using the platform&#8221; page. To get started and request an account for your project, please contact us</p>
