		<p>This data platform is built to support teams wanting to collect and analyse soil samples using the┬á<a href="https://smallholder-sha.org/protocol-1/">protocols</a>┬áat smallholder-sha.org, which are developed to support soil health assessment in smallholder management systems.</p>
